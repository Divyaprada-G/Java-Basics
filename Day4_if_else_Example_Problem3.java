// IF ELSE Example Program 3:-
// Check the Given Number is Even or Odd using if/else

public class Day4_if_else_Example_Problem3 {
    public static void main(String[] args){
         int n = 16;
         if(n%2==0){
            System.out.println("The Given Number is " + n + " Even Number");
         }else{
            System.out.println("The Given Number is " + n + " Odd Number");
         }
    }
    
}
