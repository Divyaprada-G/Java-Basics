// Here we Explore the Arithmetic Operator in Java
/*
  Arithmetic Operators are:
       Add:- (+)
       Sub:- (-)
       Mul:- (*)
       div:- (/)
       mod:- (%) it's Gives a Reminder
 */

public class Day3_Arithmetic_Operator {
    public static void main(String[] args){
          int a = 10;
          int b = 20;
          System.out.print("This Add of a and b is: ");
          System.out.println(a+b);

          System.out.print("This Sub of a and b is: ");
          System.out.println(a-b);

          System.out.print("This Mul of a and b is: ");
          System.out.println(a*b);
          
          System.out.print("This Div of a and b is: ");
          System.out.println(a/b);

          System.out.print("This Mod of a and b is: ");
          System.out.println(a%b);  
          
    }
    
}
