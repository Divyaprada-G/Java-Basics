// Here we Explore the Comparing Operator in Java
/*
  Comparing Operators are:
       Greater then:- (>)
       Lesser then:- (<)
       Greater then or equal to:- (>=)
       Lesser then or equal to:- (<=)
       equal equals to:- (==)
 */

public class Day3_Comparing_Operator {
     public static void main(String[] args){
          int num1 = 10;
          int num2 = 20;

          System.out.print("Num1 is Greater then num2: ");
          System.out.println(num1>num2);

          System.out.print("Num1 is Lesser then num2: ");
          System.out.println(num1<num2);

          System.out.print("Num1 is Greater then or equal to num2: ");
          System.out.println(num1>=num2);

          System.out.print("Num1 is Lesser then or equal to num2: ");
          System.out.println(num1<=num2);

          System.out.print("Num1 is equal equals to num2: ");
          System.out.println(num1==num2);

    }
}
